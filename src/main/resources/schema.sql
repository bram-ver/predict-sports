CREATE TABLE MATCHDAY
(
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    SEASON CHAR(9) NOT NULL
);
CREATE TABLE MATCH
    (
        ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        MATCHDAY_ID BIGINT NOT NULL,
        MATCHDAY_KEY BIGINT NOT NULL,
        CONSTRAINT FK_MATCHDAY FOREIGN KEY (MATCHDAY_ID) REFERENCES matchday(ID),
        HOME_TEAM VARCHAR,
        AWAY_TEAM VARCHAR
);
CREATE INDEX IDX_MATCHDAY_ID ON MATCH(MATCHDAY_ID);
